package st.atalhodevs.logica;

public class Palavras_Reservadas {

	public static void main(String[] args) {
		
		//break - serve para sair do loop 
		//continue - serve para avancar apos atingir uma condicao
		
		for(int i=0;i<10;i++) {
			if(i==4)continue;	
			System.out.println(i);			
		}

	}

}
